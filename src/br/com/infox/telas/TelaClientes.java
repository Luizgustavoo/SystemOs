    //Método para adicionar clientes

    private void adicionar() {
        String sql = "insert into clientes(nomecli, endcli, fonecli, emailcli, cpfcli) values ( ?, ?, ?, ?, ?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCliNome.getText());
            pst.setString(2, txtCliEnd.getText());
            pst.setString(3, txtCliFone.getText());
            pst.setString(4, txtCliMail.getText());
            pst.setString(5, txtCliCpf.getText());
            //Validação dos campos obrigatórios
            if (((txtCliNome.getText().isEmpty()) || (txtCliFone.getText().isEmpty())) || (txtCliCpf.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos campos obrigatórios!");
            } else {

                //A linha abaixo atualiza a tabela clientes com os dados do formulário
                // A estrutura abaixo é responsavel por confirmar a inserção dos dados na tabela
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Cliente adicionado com sucesso!");
                    txtCliNome.setText(null);
                    txtCliEnd.setText(null);
                    txtCliFone.setText(null);
                    txtCliMail.setText(null);
                    txtCliCpf.setText(null);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void pesquisar_cliente() {
        String sql = "select * from clientes where nomecli like ?";
        try {
            pst = conexao.prepareStatement(sql);
            // Passando o conteudo da caixa de pesquisa para o ? //Atenção ao % que é a continuação da string sql
            pst.setString(1, txtCliPesquisar.getText() + "%");
            rs = pst.executeQuery();
            //A linha abaixo usa a blibioteca rs2 para preencher a tabela
            tblClientes.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    //Método para setar o conteúdo da tabela nos campos
    public void setar_campos() {
        int setar = tblClientes.getSelectedRow(); //Pegando a linha que está selecionada com o comando getSelectedRow();
         txtCliId.setText(tblClientes.getModel().getValueAt(setar, 0).toString());
        txtCliNome.setText(tblClientes.getModel().getValueAt(setar, 1).toString());
        txtCliEnd.setText(tblClientes.getModel().getValueAt(setar, 2).toString());
        txtCliFone.setText(tblClientes.getModel().getValueAt(setar, 3).toString());
        txtCliMail.setText(tblClientes.getModel().getValueAt(setar, 4).toString());
        txtCliCpf.setText(tblClientes.getModel().getValueAt(setar, 5).toString());
        btnAdicionar.setEnabled(false);
    }
//Método par alterar os dados do cliente

    private void alterar() {
        String sql = "update clientes set nomecli=?,  endcli=?,  fonecli=?,  emailcli=?,  cpfcli=?  where idcli=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCliNome.getText());
            pst.setString(2, txtCliEnd.getText());
            pst.setString(3, txtCliFone.getText());
            pst.setString(4, txtCliMail.getText());
            pst.setString(5, txtCliCpf.getText());
            pst.setString(6, txtCliId.getText());

            if (((txtCliNome.getText().isEmpty()) || (txtCliFone.getText().isEmpty())) || (txtCliCpf.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos campos obrigatórios!");
            } else {

                //A linha abaixo atualiza a tabela clientes com os dados do formulário
                // A estrutura abaixo é responsavel por confirmar a alteração dos dados na tabela
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Dados do cliente alterados com sucesso!");
                    txtCliNome.setText(null);
                    txtCliEnd.setText(null);
                    txtCliFone.setText(null);
                    txtCliMail.setText(null);
                    txtCliCpf.setText(null);
                    btnAdicionar.setEnabled(true);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    //Remove clientes 
    private void remover(){
        //A estrutura abaixo confirma a remoção do cliente
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover esse cliente?", "Atenção", JOptionPane.YES_NO_OPTION);
        if(confirma == JOptionPane.YES_OPTION){
            String sql = "delete from clientes where idcli=?";
            try {
                pst=conexao.prepareStatement(sql);
                pst.setString(1, txtCliId.getText());
               int apagado = pst.executeUpdate();
               if(apagado>0){
                   JOptionPane.showMessageDialog(null, "Cliente removido com sucesso!");
                    txtCliNome.setText(null);
                    txtCliEnd.setText(null);
                    txtCliFone.setText(null);
                    txtCliMail.setText(null);
                    txtCliCpf.setText(null);
                    btnAdicionar.setEnabled(true);
               }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

