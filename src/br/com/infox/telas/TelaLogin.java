    public void logar() {
        String sql = "select * from usuarios where login= ? and senha =?";
        try {
            //Preparam a consulta ao banco de dados e captura o que foi digitado, o "?" é substituido pelo valor da variavel
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtSenha.getText());
            //A linha abaixo realiza a consulta ao query ou seja ao banco e dados
            rs = pst.executeQuery();

            // A esrutura abaixo faz o tratamento do perfil do usuario
            //Se existir correspondencia de usuario e senha faça abaixo
            if (rs.next()) {
                //Obtem o que está dentro da tabela perfil
                String perfil = rs.getString(6);
                // A esrutura abaixo faz o tratamento do perfil do usuario
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menRel.setEnabled(true); //Abilita a tela de relatórios para o usuarios administradores do sistema
                    TelaPrincipal.menCadUsu.setEnabled(true); //Abilita a tela de usuários para o usuarios administradores do sistema
                    TelaPrincipal.lblUsuario.setText(rs.getString(2)); // Atribui a label de lblUsuario o texto obtido na ordem do BDD no caso o usuario cadastrado
                    TelaPrincipal.lblUsuario.setForeground(Color.red); //Atribui a cor vermelha se o usuario tiver permissãoes de administrador
                    this.dispose();
                    conexao.close();
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(2));
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "usuario e/ou senha inválidos (s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

